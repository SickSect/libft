#include "libft.h"

void	*ft_memmove(void *dest, void *src, size_t n)
{
	int	i;

	if ((dest == src) || n == 0)
		return (dest);
	if (dest > src)
	{
		i = (int)n - 1;
		while (i >= 0 && i < (int)n)
		{
			*(char *)(dest + i) = *(char *)(src + i);
			i--;
		}
	}
	else
	{
		i = 0;
		while (i < (int)n)
		{
			*(char *)(dest + i) = *(char *)(src + i);
			i++;
		}
	}
	return (dest);
}
